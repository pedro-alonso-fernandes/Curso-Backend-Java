package oo.heranca.desafio;

public interface Esportivo {

	// Uma interface serve para ser implementada à uma classe. Ao implementar uma 
	// interface à uma classe, a classe é obrigada a ter todos os metódos que estão contidos
	// na interface.
	
	// Mesmo não especificando nada, os métodos definidos na interface são "public" e "abstract"
	// por padrão.	
	void ligarTurbo();
	void desligarTurbo();
}
